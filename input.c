#include <stdio.h>

int main() {
    int radius;

    printf("Please enter a radius: ");
    scanf("%i", &radius);

    float area = 3.14 * (radius^2);
    printf("The area of a circle with %i radius is %f\n", radius, area);
    return 0;
}
